DROP TABLE IF EXISTS alarm;
DROP TABLE IF EXISTS dump;

CREATE TABLE alarm (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  devid VARCHAR (64),
  deveui CHAR(16),
  created TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  action VARCHAR(16),
  level SMALLINT ,
  message VARCHAR(255)
);

CREATE TABLE dump (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  created TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  url TEXT,
  dumped TEXT
);
